//Create datatypes.js file and use the JavaScript typeof operator to check different data types. Check the data type of each variable

let intNum = 10;
let userName = "Sourabha";
let isStudent = true;
let age;
let city = null;

// Print the data types : typeof(value) or typeof value is used to check the data type of variable
console.log("intNum:", typeof intNum);         // "number"
console.log("userName:", typeof userName);     // "string"
console.log("isStudent:", typeof isStudent);   // "boolean"
console.log("age:", typeof age);               // "undefined"
console.log("city:", typeof city);             // "object"